package com.erbis.java.courses.max.lesson12;

abstract public class Sound {

    abstract public String sounds();

    public String dogSounds() {
        return "Gav-Gav";
    }

    public String catSounds() {
        return "Meow-meow";
    }

    public String crowSounds() {
        return "Karr-Karr";
    }
}
